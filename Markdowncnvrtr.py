def create_dynamic_listing_model(field_names: List[str]) -> Type[BaseModel]:
    """Creates a dynamic Pydantic model based on field names."""
    field_definitions = {field.strip(): (str, ...) for field in field_names}
    return create_model('DynamicListingModel', **field_definitions)

def create_listings_container_model(listing_model: Type[BaseModel]) -> Type[BaseModel]:
   """Creates a container model for listings."""
   return create_model('DynamicListingsContainer', listings=(List[listing_model], ...))
def html_to_markdown_with_readability(raw_html: str) -> str:
    """Converts HTML to markdown format with improved readability."""
    try:
        markdown_converter = html2text.HTML2Text()
        markdown_converter.ignore_links = False
        markdown_converter.ignore_images = False
        markdown_converter.ignore_emphasis = False
        markdown_converter.body_width = 0
        return markdown_converter.handle(raw_html).strip()
    except Exception as e:
        raise ValueError(f"Failed to convert HTML to markdown: {str(e)}")


def split_text_into_chunks(text: str, chunk_size: int) -> List[str]:
    """Splits text into chunks while preserving complete entries."""
    words = text.split()
    chunks = []
    current_chunk = []
    current_size = 0
    
    for word in words:
        word_size = len(word) + 1
        if current_size + word_size > chunk_size and current_chunk:
            chunks.append(' '.join(current_chunk))
            current_chunk = [word]
            current_size = word_size
        else:
            current_chunk.append(word)
            current_size += word_size
    
    if current_chunk:
        chunks.append(' '.join(current_chunk))
    
    return chunks